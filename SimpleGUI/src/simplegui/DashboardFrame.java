        if (name == null || ageStr == null || name.isEmpty() || ageStr.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Name and Age are required!");
            return;
        }

        try {
            int age = Integer.parseInt(ageStr);

            try (Connection conn = connectionDB.getConnection(); PreparedStatement pst = conn.prepareStatement(
                    "INSERT INTO players (user_id, name, age, position, market_value, best_role) VALUES (?, ?, ?, ?, ?, ?)")) {

                pst.setInt(1, userId);
                pst.setString(2, name);
                pst.setInt(3, age);
                pst.setString(4, position);
                pst.setString(5, value);
                pst.setString(6, role);

                pst.executeUpdate();
            }

            loadPlayers();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Age must be a number!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
