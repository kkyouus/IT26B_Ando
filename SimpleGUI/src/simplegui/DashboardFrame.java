    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = tblPlayers.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select a player!");
            return;
        }

        int id = (int) tblPlayers.getValueAt(row, 0);

        int confirm = JOptionPane.showConfirmDialog(this,
                "Delete this player?",
                "Confirm",
                JOptionPane.YES_NO_OPTION);

        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        try (Connection conn = connectionDB.getConnection(); PreparedStatement pst = conn.prepareStatement(
                "DELETE FROM players WHERE id=? AND user_id=?")) {

            pst.setInt(1, id);
            pst.setInt(2, userId);
            pst.executeUpdate();

            loadPlayers();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Delete error: " + e.getMessage());
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

